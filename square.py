#find out the area and perimeter of a square?
x=10
#by f string
print(f"area of a square with side value {x} is {x*x}")
print(f"perimeter of a square with side value {x} is {4*x}")
#by dot format
print("area of a square with side value {} is {}".format(x,x*x))
print("perimeter of a square with side value {} is {}".format(x,4*x))
